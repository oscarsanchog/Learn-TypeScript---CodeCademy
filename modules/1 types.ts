//! TIPOS de TS:
/* 1. number: Representa tanto números enteros como de punto flotante.
2. string: Representa datos de texto.
3. boolean: Representa valores booleanos true o false.
4. any: Representa cualquier tipo de valor, lo que permite la omisión de comprobaciones de tipo durante la compilación.
5. void: Se usa en funciones que no devuelven ningún valor.
6. null y undefined: Tienen sus propios tipos null y undefined, respectivamente.
7. array: Representa una matriz de valores del mismo tipo.
8. tuple: Permite expresar una matriz con un número fijo de elementos cuyos tipos son conocidos, pero no necesariamente idénticos.
9. enum: Permite definir un conjunto de constantes nombradas.
10 object: Representa cualquier valor no primitivo.
11. never: Representa el tipo de valores que nunca ocurren. Por ejemplo, una función que siempre arroja una excepción.
12. unknown: Similar a any, pero más seguro, ya que obliga a realizar comprobaciones de tipo antes de operar con valores de este tipo. */











